export const getFallbackTajweedData = (ruleNo = "0") => {
  const normalizedRule = (ruleNo ?? "0").replace(/_/g, ".");

  if (normalizedRule === "0" || normalizedRule === "") {
    return FALLBACK_TAJWEED_MAIN_RULES;
  }

  const primaryRuleKey = normalizedRule.split(".")[0];
  return FALLBACK_TAJWEED_SUB_RULES[primaryRuleKey] || [];
};

